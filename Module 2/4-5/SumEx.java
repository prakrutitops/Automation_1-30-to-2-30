package com.a45;

public class SumEx 
{
	public static void main(String[] args) 
	{
		
		int a = 10;
		int b = 5;
		
		//int - data type -numeric value
		//a - variable
		//10 - value
		
		
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = a/b;
		int g = 10%5;
		
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		
		
	}
}
